// NOT Operator 
// AND Operator
// OR Operator
// XOR Operator (Exclusive Or)

// Use the bool keyword
bool csharpIsCool = false;
csharpIsCool = true;

// Boolean Logical Operators
// Logical NOT Operator, Negation Operator
bool isTrue =!false;
bool isFalse =!true;

//AND Operator
bool trueAndExample = true && true;
bool falseAndExample = true && false;

// OR Operator
bool trueOrExample = true || false;
bool falseOrExample = false || false;

// XOR Operator (Exclusive Or) Only can be true
bool trueExclusiveOr = true ^ false;
bool falseExclusiveOr = true ^ true;

// (condition 1 && not condition 2) || (not condition 1 && condition 2)

// Comparison Operators
// Equal, not Equal, Greater than, and Less Than
bool isGreaterThan = 10 > 10; 
bool isGreaterThanOrEqualTo = 10 >= 10;

// Equality in C# uses 2 equal signs never 3
bool equality = 17 == 9;
bool inequality = 17 != 9;



